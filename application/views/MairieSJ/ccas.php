    <div class="col-12 col-xl-6 mt-3">
        <p class="ml-3 mr-3">Le C.C.A.S est un établissement public communal, chargé de mettre en œuvre la politique d’action sociale de la municipalité.</p>
        <p class="ml-3 mr-3">Le rôle premier du CCAS est d’accueillir, d’écouter et d’orienter les personnes en difficultés. Le C.C.A.S a pour mission d’informer le public sur les différents dispositifs sociaux existants sur la commune et anime une action générale de prévention et de développement social en liaison étroite avec les partenaires institutionnels et privés.</p>
        <p class="ml-3 mr-3">Il vous aide également pour toutes autres démarches telles que les dossiers MDPH, APA, télé alarme, retraite, dossiers de Surendettements, CAF … et gère l’épicerie sociale.</p>
        <p class="ml-3 mr-3"><FONT color="red">L’aide aux familles :</FONT></p>
        <p class="ml-3 mr-3">Une épicerie sociale est mise à disposition des personnes en difficultés. L’accès à l’épicerie dépend des conditions de ressources des familles St Justoises et est étudié lors d’un entretien avec Mme Catherine BONNET, adjointe en charge des affaires sociales.</p>
        <p class="ml-3 mr-3">Parallèlement à cela, l’épicerie sociale met en place des ateliers d’informations, de sensibilisation et d’échanges autour des thèmes tels que la santé, l’équilibre alimentaire, la citoyenneté, le logement… ces ateliers sont accessibles au public aidé.</p> 
        <p class="ml-3 mr-3">Par ailleurs, l’épicerie sociale tente de créer un lieu convivial d’échange et de partage autour de l’alimentation et ainsi favoriser la création de lien social et le maintien de la dignité de chaque personne aidée.</p>
        <p class="ml-3 mr-3">Tous les ans, un arbre de Noël est organisé par le CCAS pour les enfants dont les parents sont bénéficiaires de l’épicerie sociale, un moment magique où les enfants ont le plaisir de recevoir un cadeau du Père Noël.</p>
        <p class="ml-3 mr-3">Des sorties sont également organisées par le CCAS pour les parents et enfants bénéficiaires de l’épicerie sociale. En 2018, ils ont pu se rendre au parc aquatique « Nausicaa » pour un tarif de 3€ par personne.</p>
        <p class="ml-3 mr-3"><b>Partenariats :</b></p>
        <p class="ml-3 mr-3">La mutuelle communale « MOAT » :</p>
        <p class="ml-3 mr-3">Une collaboration a été mise en place avec votre CCAS, afin de permettre l’accès à une mutuelle simple et à bas prix. Les dossiers d’adhésions sont disponibles à tout moment au CCAS.</p>
