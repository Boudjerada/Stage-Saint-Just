<p class="d-flex justify-content-center ml-3 mr-3">L’accueil de loisirs sans hébergement est une entité éducative habilitée à accueillir les enfants de manière habituelle et collective pendant les vacances scolaires. Il accueille sur une année à St Just près de 200 enfants de 3 à 13 ans, soit de la petite section maternelle à la 5ème du collège. Ces enfants sont de milieux sociaux différents, résidants principalement à St Just mais venant aussi des villages du Plateau Picard.</p>
<p class="d-flex justify-content-center ml-3 mr-3"><b>Durant les petites vacances, l’effectif varie :</b></p>
<p class="d-flex justify-content-center ml-3 mr-3">- Pour les 3 à 5 ans : de 25 à 35 enfants</p>
<p class="d-flex justify-content-center ml-3 mr-3">- Pour les 6 à 13 ans : de 90 à 120 enfants</p>
<p class="d-flex justify-content-center ml-3 mr-3"><b>Pendant les grandes vacances :</b></p>
<p class="d-flex justify-content-center ml-3 mr-3">La capacité d’accueil peut atteindre un total de près de 200 enfants et jeunes adolescents dans toute la structure. L’ALSH se situe près d’un espace de loisirs où se trouvent, à disposition, les équipements suivants :</p>
<p class="d-flex justify-content-center ml-3 mr-3">Un gymnase de 1500 m2, 2 dojos, un terrain de foot, de basket et pétanque, une rivière et un étang pour la pêche, le bois de Mermont pour les grands jeux comme la chasse à l’œuf, une médiathèque, une salle des fêtes, une cantine et une grande prairie clôturée.</p>
