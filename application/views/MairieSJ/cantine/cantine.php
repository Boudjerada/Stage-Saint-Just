        <h3 class="d-flex justify-content-center">REGLEMENT DE LA CANTINE</h3>
        
        <h6 class="d-flex justify-content-center">Dispositions générales :</h6>
        
        <p class="ml-3 mr-3">La ville de Saint Just en Chaussée met à disposition des enfants des écoles publiques de St Just une restauration pour le repas de midi. La cantine est ouverte à tous les enfants des écoles élémentaires et maternelles suivant la capacité d'accueil de restauration, la limite des places disponibles et dans le respect des normes de sécurité en vigueur.<br>
           Le temps du repas est un moment privilégié pour les enfants: alimentation, éducation du goût, moment d'échanges.<br>
           La cantine municipale est placée sous la responsabilité de Monsieur le Maire, ou de son adjoint. Elle fonctionne pendant les journées scolaires et les journées d'activité du périscolaire et du centre aéré.</p>
        
        <h5 class="d-flex justify-content-center">Modalités de réservation :</h5>
 
        <p class="ml-3 mr-3">Tous les enfants doivent être préalablement inscrits à la mairie pour bénéficier de la restauration scolaire. Il n’y a pas d’achat de tickets mais une réservation de repas avec paiement à faire auprès du service de restauration au secrétariat de mairie. Les réservations des repas de cantine se feront mois par mois, sur le document papier «planning de réservation mensuel de repas scolaires» avant la date butoir indiquée en annexe. Ces formulaires prédéfinis peuvent soit :<br>
            - être retirés sur le présentoir situé dans le hall de la mairie ou à l'accueil<br>
            - sur internet</p>
 
        <h6 class="ml-3 mr-3">ATTENTION</h6>
        <p  class="ml-3 mr-3">Les changements en cours de mois ou hors délais ne sont plus acceptés.</p>
  
 
        <p  class="ml-3 mr-3">La réservation des repas permet :<br>
                    - d'établir l'organisation des salles de restaurant<br>
                    - de définir l'accompagnement et la surveillance des enfants sur chaque restaurant<br>
                    - de commander les quantités suffisantes auprès du prestataire de service<br>
                    - de minimiser le gaspillage (un repas non consommé est un repas jeté)<br><br>
 
    Les repas prévus par les parents valent un engagement et sont facturés, à l'exception d'une absence pour maladie justifiée par un certificat médical ou fonctionnement inhabituel de l'accueil scolaire.<br>
 
    A titre exceptionnel et ce, dès le 2ème jour d'absence du professeur des écoles, non remplacé à son poste, le coût prévisionnel des repas non consommés les jours suivants ne peut être imputé aux parents.<br>
    Le remboursement des repas payés se fera par décompte sur le mois suivants (décision du conseil municipal du 8 décembre 2017).<br><br>
  
 
    Les paiements différés ne sont plus acceptés.<br>
    Le paiement se fait au moment de la réservation, soit par chèque ou en espèces. Sans règlement, aucune réservation ne sera prise en compte.</p><br><br>
 
    <h6 class="ml-3 mr-3">Les tarifs:</h6>
    <p  class="ml-3 mr-3">Les tarifs de restauration pour les enfants Saint Justois et pour les enfants extérieurs à la commune sont fixés chaque année par le Conseil Municipal.</p><br>
 
    <h6 class="ml-3 mr-3">Règles de vie à suivre par les enfants:</h6>
    <p  class="ml-3 mr-3">
        • Bien se comporter durant le trajet, ne pas frapper aux portes, ne pas arracher des fleurs, ne pas crier...<br>
 
         • Ne pas chahuter pour éviter tout risque d'accident en pénétrant dans le restaurant scolaire;<br>
 
        • Aller aux W.C. Avant de se rendre au repas pour éviter d'avoir à se déplacer durant le repas;<br>
 
        • Se laver les mains avec du savon avant de passer à table, pour chasser tous les microbes qui sont sur les mains;<br>
 
        • Manger dans le calme pour ne pas déranger les autres et pour que le repas soit bien digéré après;<br>
 
        • Se tenir correctement à table, parce que le temps du repas n'est pas celui du sport ou de la récréation;<br>
 
        • Goûter à tout ce qui est proposé. Le goût s'éduque et évolue: goûter à tout, c'est respecter le personnel qui l'a préparé. Manger de tout permet de rester en bonne santé;<br>
 
        • Ne pas jouer avec la nourriture, les couverts et les ustensiles mis sur les tables;<br>
 
        • Respecter ses voisins de table, le personnel de service et de surveillance;<br>
 
        • Participer au rangement: regrouper les couverts au centre de la table sans se lever de sa chaise;<br>
 
        • Respecter le matériel de restauration (assiette – verre – couverts – tables – chaises) le matériel cassé ou emporté volontairement sera facturé à la famille;<br>
 
        • Ne pas apporter d'aliments de boissons ou d'objets personnels à la cantine.</p><br>
 
    <h6 class="ml-3 mr-3">Régimes alimentaires :</h6>
    <p  class="ml-3 mr-3">La restauration scolaire ayant une vocation collective, elle ne peut répondre aux régimes diététiques particuliers (allergie – contre-indications médicales). Toutefois, la restauration pourra accepter les enfants dont le régime est compatible avec les possibilités du service de restauration scolaire, dans le cadre d'un Projet d'Accueil individualisé (P.A.I.). Cette démarche doit être engagée par la famille auprès du médecin scolaire. Un exemplaire de ce P.A.I., validé par le médecin scolaire, sera retourné au service scolaire, visé par la famille. Le personnel de service recevra toutes les informations nécessaires au respect de ce P.A.I. Dans la mesure où des troubles de santé de cette nature apparaîtraient en cours d'année, le gestionnaire peut, après une mise en demeure, exclure l'enfant du restaurant scolaire tant que la famille n'aura pas engagé les démarches nécessaires.</p><br>
 
    <h6 class="ml-3 mr-3">Menus de remplacement :</h6>
    <p  class="ml-3 mr-3">La ville propose des menus de remplacement (sans porc, sans viande) mais ne prend pas en compte les contraintes religieuses dans la composition des repas de restauration scolaire (viande hala, casher...)</p><br>
 
    <h6 class="ml-3 mr-3">Gestion et comportements perturbateurs ou incorrects des enfants :</h6>
    <p  class="ml-3 mr-3">Tout élève perturbateur sera sanctionné. (acte d'incivilité verbale ou physique, insolence, bagarre, détérioration de matériels...) Après deux lettres d'avertissements, l'enfant et ses parents seront convoqués devant la commission de cantine et mis devant leurs responsabilités respectives.</p>
    <h6 class="ml-3 mr-3">Composition de la commission :</h6>
    <p  class="ml-3 mr-3">
    - Le Maire ou son adjoint<br>
    - Le directeur de l'école<br>
    - Les surveillants de cantine<br>
    - Un représentant des parents d'élèves<br>
    • A l'issue, une exclusion temporaire ou définitive pourra être prononcée.</p><br>
  
    <h6 class="ml-3 mr-3">Incident ou accident :</h6>
    <p  class="ml-3 mr-3">
        En cas d'incident bénin, le responsable désigné par la famille est prévenu par téléphone, le directeur de l'école est informé. En cas d'événement grave, accidentel ou non, mettant en péril ou compromettant la santé de l'enfant, le service prend toutes les dispositions nécessaires (médecin, pompier ou SAMU). Le responsable légal est immédiatement informé.<br>
        C'est pour cette raison qu'il est très important de compléter la fiche sanitaire et de la transmettre au service de la cantine en Mairie.</p><br>
 
 
    <h6 class="ml-3 mr-3">Médicaments :</h6>
    <p  class="ml-3 mr-3">
        Le service n'est pas autorisé à administrer des médicaments ou des soins particuliers courants, sauf si un P.A.I. le prévoit. Sauf circonstances exceptionnelles, la prise de médicaments ne pourrait se faire qu'en fournissant l'ordonnance ou sa photocopie, et un mot explicatif signé par les parents indiquant précisément la posologie et l'horaire de prise du médicament.<br>
        Cette facilité accordée aux parents ne peut être que ponctuelle et en aucun cas remplacer un P.A.I. Leur responsabilité reste pleine et entière.</p>
                    
